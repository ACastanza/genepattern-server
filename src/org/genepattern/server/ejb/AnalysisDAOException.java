package org.genepattern.server.ejb;

/**
 * AnalysisDAOException.java
 * 
 * @author rajesh kuttan
 * @version
 */
public class AnalysisDAOException extends java.lang.Exception {

	/**
	 * Creates new <code>AnalysisDAOException</code> without detail message.
	 */
	public AnalysisDAOException() {
	}

	/**
	 * Constructs an <code>AnalysisDAOException</code> with the specified
	 * detail message.
	 * 
	 * @param msg
	 *            the detail message.
	 */
	public AnalysisDAOException(String msg) {
		super(msg);
	}
}

